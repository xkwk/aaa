CREATE SCHEMA IF NOT EXISTS ACTUARIAL_AI_DEV.APP;

CREATE TABLE IF NOT EXISTS ACTUARIAL_AI_DEV.RAW.FILE_MANIFEST (
  RUN_ID STRING,
  FILE_ID STRING,
  RELATIVE_PATH STRING,
  FILE_NAME STRING,
  ROLE STRING,
  SHA256 STRING,
  SIZE_BYTES NUMBER,
  MODIFIED_AT_UTC STRING,
  LOADED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

