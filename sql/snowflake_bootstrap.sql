  RUN_ID STRING,
  VALUATION_PERIOD STRING,
  PRODUCT STRING,
  PORTFOLIO STRING,
  COHORT STRING,
  MEASURE STRING,
  AMOUNT NUMBER(38, 10),
  CURRENCY STRING,
  LOADED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

